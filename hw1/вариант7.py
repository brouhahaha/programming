print('Введите три числа в столбик')
a = int (input())
b = int (input())
c = int (input())

if a/b==c :
    print ( 'условие 5 соблюдается (a/b=c)')
else:
    print ( 'условие 5 не соблюдается (a/b≠c)')

if a**b==c :
    print ( 'условие 6 соблюдается (a^b=c)')
else:
    print ( 'условие 6 не соблюдается (a^b≠c)')
